
  
    function displayLeftNav() {
      var x = document.getElementById("left-nav");
      if (x.style.display === "none") {
        x.style.display = "block";
        window.scrollTo(0,0);
      } else {
        x.style.display = "none";
        window.scrollTo(0,0);
      }
    } 

